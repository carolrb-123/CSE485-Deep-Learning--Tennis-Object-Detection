# ─── HELPERS ───
def load_yaml_classes(dataset_dir):
    yaml_path = Path(dataset_dir) / "data.yaml"
    with open(yaml_path) as f:
        data = yaml.safe_load(f)
    names = data.get("names", [])
    if isinstance(names, dict):
        names = [names[i] for i in sorted(names.keys())]
    return names

def build_index_remap(source_classes):
    remap = {}
    print("\n  Source class → Master class mapping:")
    for i, name in enumerate(source_classes):
        master_name = CLASS_ALIASES.get(name)
        if master_name and master_name in MASTER_CLASSES:
            master_idx = MASTER_CLASSES.index(master_name)
            print(f"    [{i}] '{name}' → [{master_idx}] '{master_name}'")
        else:
            master_idx = None
            print(f"    [{i}] '{name}' → DROPPED")
        remap[i] = master_idx
    return remap

def remap_label_file(src_path, dst_path, remap):
    kept, dropped = 0, 0
    out_lines = []
    with open(src_path) as f:
        for line in f:
            line = line.strip()
            if not line:
                continue
            parts = line.split()
            master_idx = remap.get(int(parts[0]))
            if master_idx is None:
                dropped += 1
                continue
            parts[0] = str(master_idx)
            out_lines.append(" ".join(parts))
            kept += 1
    if out_lines:
        os.makedirs(dst_path.parent, exist_ok=True)
        with open(dst_path, "w") as f:
            f.write("\n".join(out_lines) + "\n")
    return kept, dropped

def collect_pairs(dataset_dir, split):
    base = Path(dataset_dir)
    img_dir = base / "images" / split
    lbl_dir = base / "labels" / split
    if not img_dir.exists():
        img_dir = base / split / "images"
        lbl_dir = base / split / "labels"
    if not img_dir.exists():
        return []
    pairs = []
    for img_path in img_dir.iterdir():
        if img_path.suffix.lower() not in {".jpg", ".jpeg", ".png", ".bmp"}:
            continue
        lbl_path = lbl_dir / (img_path.stem + ".txt")
        if lbl_path.exists():
            pairs.append((img_path, lbl_path))
    return pairs

