# and wiping them before moving is harmful.

# Now copy (or move) files to their new locations based on the resplit
for split, pair_list in new_splits_data.items():
    for folder in ["images", "labels"]:
        (out / folder / split).mkdir(parents=True, exist_ok=True) # Ensure destination directories exist

