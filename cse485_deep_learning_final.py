total_kept = total_dropped = total_images = 0
seen_names = {"train": set(), "val": set(), "test": set()}

for ds_idx, ds_dir in enumerate(DATASET_DIRS):
    print(f"\n{'='*50}")
    print(f"Processing dataset {ds_idx+1}: {ds_dir}")
    print(f"{'='*50}")
    source_classes = load_yaml_classes(ds_dir)
    print(f"  Classes found: {source_classes}")
    remap = build_index_remap(source_classes)

    for split in ["train", "val", "test"]:
        pairs = collect_pairs(ds_dir, split)
        if not pairs:
            print(f"  [{split}] No data found, skipping.")
            continue
        print(f"  [{split}] {len(pairs)} image-label pairs")
        for img_path, lbl_path in pairs:
            stem = f"ds{ds_idx}_{img_path.stem}"
            unique_stem = stem
            counter = 1
            while unique_stem in seen_names[split]:
                unique_stem = f"{stem}_{counter}"
                counter += 1
            seen_names[split].add(unique_stem)
            dst_img = out / "images" / split / (unique_stem + img_path.suffix)
            dst_lbl = out / "labels" / split / (unique_stem + ".txt")
            shutil.copy2(img_path, dst_img)
            k, d = remap_label_file(lbl_path, dst_lbl, remap)
            total_kept += k
            total_dropped += d
            total_images += 1

print(f"\nMerge done: {total_images} images, {total_kept} annotations kept, {total_dropped} dropped")

